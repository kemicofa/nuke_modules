import { $ } from "zx";

/**
 * Function that takes a path to a directory and searches for
 * all node_modules. It avoids matching nested node_modules.
 *
 * @param startDir the starting directory path to start searching for node_modules
 */
export async function listNodeModules(startDir: string) {
  const { stdout } =
    await $`find ${startDir} -type d -name node_modules -prune`;

  const cleaned = stdout.trim();

  if (!cleaned) {
    return [];
  }

  return cleaned.split(/\n+/g);
}

const parseSizeReg = /^(\d+|\d+\.\d+)([BKMGT]?)$/i;
const UNITS = {
  B: 1,
  K: 1024,
  M: 1024 ** 2,
  G: 1024 ** 3,
  T: 1024 ** 4,
};

function isUnit(value: string): value is keyof typeof UNITS {
  return value in UNITS;
}

/**
 * Function that attempts to calculate the bytes
 * of a given node_modules directory.
 *
 * @param path to the node_modules directory
 */
export async function getSizeOfDirectory(path: string): Promise<{
  /**
   * The bytes deduced from the Size and Unit returned by the shell script
   */
  bytes: number;
  /**
   * Raw size value returned by shell script
   */
  size: number;
  /**
   * Raw unit size returned by shell script
   */
  unit: "B" | "K" | "M" | "G" | "T";
} | null> {
  const { stdout } = await $`du -sh ${path} | awk '{print $1}'`;
  if (!stdout) {
    return null;
  }
  const match = stdout.trim().match(parseSizeReg);
  if (!match) {
    return null;
  }

  const size = parseFloat(match[1]);
  const unit = match[2].toUpperCase() || "B"; // If there are no units, assume that it's just Bytes

  if (!isUnit(unit)) {
    return null;
  }

  return {
    bytes: UNITS[unit] * size,
    size,
    unit,
  };
}
